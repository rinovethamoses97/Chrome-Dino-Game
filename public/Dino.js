class Dino{
    constructor(image){
        this.width=50;
        this.height=60;
        this.pos=createVector(100,height-2-this.height);
        this.velcoity=createVector(0,0);
        this.gravity=createVector(0,0);
        this.acceleration=createVector(0,0);
        this.inAir=false;
        this.score=0;
        this.img=image;
    }
    show(){
        stroke(0);
        fill(0);
        // rect(this.pos.x,this.pos.y,this.width,this.height);
        image(this.img,this.pos.x,this.pos.y,this.width,this.height);
    }
    update(){
        this.acceleration.add(this.gravity);
        this.velcoity.add(this.acceleration);
        this.pos.add(this.velcoity);
        this.acceleration.x=0;
        this.acceleration.y=0; 
        if(this.pos.y+this.height>=height-2){
            this.velcoity.y=0;
            this.gravity.y=0;
            this.inAir=false;
            this.pos.x=100;
            this.pos.y=height-2-this.height;
        }
    }
    jump(){
        this.inAir=true;
        this.gravity.y=0.3;
        this.velcoity.y=-10;    
    }
    hits(obstacle){
        return collideRectRect(this.pos.x,this.pos.y,this.width,this.height,obstacle.pos.x,obstacle.pos.y,obstacle.width,obstacle.height);
    }
}